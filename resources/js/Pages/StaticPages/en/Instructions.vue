                        Ground-based photo monitoring is a fast, simple, and effective way to determine relatively slow changes in nature. They will build up into a valuable record. Photo monitoring may be divided into two directions:
                        <ol class="list-decimal ml-5">
                            <li>comparison photos, whereby a photograph is used to compare a known condition or to estimate some parameter (e.g. water level)</li>
                            <li>repeat photographs, whereby several pictures are taken of the exact same location over time to detect change. Comparing recent photographs with older ones provides valuable historical evidence, the photos will show landscape, vegetation, shoreline, human influence etc.</li>
                        </ol>
                    </div>
                    <div class="my-3">
